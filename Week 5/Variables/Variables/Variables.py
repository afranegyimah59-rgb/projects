# Variable - A containers for a value (string, integer, float, boolean)
#            A variable behaves as if it was the value it contains

# Strings
first_name = "Bro"
food = "pizza"
email = "Bro123@fake.com"

# Integers
age = 25
quantity = 3
num_of_students = 30

# Float
price = 10.99
gpa = 3.2
distance = 5.5

# Boolean
is_student = True
for_sale = False
is_online = True

print(first_name)
print(food)
print(email)
